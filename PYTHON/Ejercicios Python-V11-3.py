
"""
	Ejercicio 3:
		• Crea un programa que pida tres números por teclado. 
		  El programa imprime en consola la media aritmética de 
		  los números introducidos.
"""

num1 = int(input("INGRESE NUM1: "))
num2 = int(input("INGRESE NUM2: "))
num3 = int(input("INGRESE NUM3: "))


m_arit = (num1 + num2 + num3) / 3

print("La medía aritmética es: " + str(m_arit))




